// -------------------------
// Tunable thresholds
// -------------------------
const int FRONT_BELOW_BALL_MIN = 1400;
const int FRONT_BELOW_BALL_MAX = 3600;
const int FRONT_UPPER_OBSTACLE_MIN = 1200;
const int CENTER_BALL_COLLECT_MIN = 1200;
const int CENTER_BALL_RISE_MARGIN = 20;
const int BACK_DEPOSIT_TRIGGER_MIN = 2400;

// -------------------------
// Tunable motor powers
// -------------------------
const int COLLECTOR_POWER = 127;
const int DRIVE_APPROACH_POWER = 95;
const int DRIVE_CREEP_POWER = 65;
const int DRIVE_SCAN_POWER = 55;
const int DRIVE_AVOID_POWER = 75;
const int INITIAL_BALL_SEARCH_TIMEOUT_MS = 1800;
const int INITIAL_BALL_CONFIRM_COUNT = 2;
const int DRIVE_BOUNDARY_REVERSE_POWER = 85;
const int DRIVE_BOUNDARY_REVERSE_MS = 1500;
const int BOUNDARY_ALIGN_TURN_POWER = 55;
const int ALIGN_TURN_POWER = 65;
const int REVERSE_POWER = 80;
const int REVERSE_CORRECT_POWER = 55;
const int GATE_OPEN_SPEED = 100;
const int GATE_CLOSE_SPEED = -100;
const int GATE_MOVE_MS = 550;

int scanDirection = 1; // 1 = CCW scan, -1 = CW scan
int lastScanSwapTime = 0;
int centerEmptyBaseline = 0;
int centerBaselineReady = 0;

void setDrive(int leftPower, int rightPower) {
  motor[leftWheel] = leftPower;
  motor[rightWheel] = rightPower;
}

void stopDrive() {
  setDrive(0, 0);
}

void rotateCW(int power) {
  setDrive(power, -power);
}

void rotateCCW(int power) {
  setDrive(-power, power);
}

void runCollector(int enabled) {
  if (enabled) {
    motor[collectorMotor] = COLLECTOR_POWER;
  } else {
    motor[collectorMotor] = 0;
  }
}

void openGate() {
  motor[gateMotor] = GATE_OPEN_SPEED;
  wait1Msec(GATE_MOVE_MS);
  motor[gateMotor] = 0;
}

void closeGate() {
  motor[gateMotor] = GATE_CLOSE_SPEED;
  wait1Msec(GATE_MOVE_MS);
  motor[gateMotor] = 0;
}

int anyLineDetected() {
  return (SensorValue[frontLeftLine] == 0 ||
          SensorValue[frontRightLine] == 0 ||
          SensorValue[backLeftLine] == 0 ||
          SensorValue[backRightLine] == 0);
}

int isBallCandidate() {
  int lower = SensorValue[frontBelowLight];
  int upper = SensorValue[frontUpperLight];
  return (lower >= FRONT_BELOW_BALL_MIN &&
          lower <= FRONT_BELOW_BALL_MAX &&
          upper < FRONT_UPPER_OBSTACLE_MIN);
}

int isObstacleCandidate() {
  return SensorValue[frontUpperLight] >= FRONT_UPPER_OBSTACLE_MIN;
}

int readCompassDirectionIndex() {
  // Compass outputs are active-low: 0 means that direction is active.
  if (SensorValue[noth] == 0) return 0; // N
  if (SensorValue[east] == 0) return 1; // E
  if (SensorValue[south] == 0) return 2; // S
  if (SensorValue[west] == 0) return 3; // W
  return -1;
}

void updateCenterBaselineIfEmpty() {
  int center = SensorValue[centerLight];

  // Keep baseline from no-ball readings only, so a loaded ball does not shift it upward.
  if (center > CENTER_BALL_COLLECT_MIN) {
    return;
  }

  if (!centerBaselineReady) {
    centerEmptyBaseline = center;
    centerBaselineReady = 1;
    return;
  }

  // Smooth baseline to absorb slow drift while still reacting to real environment change.
  centerEmptyBaseline = ((centerEmptyBaseline * 7) + center) / 8;
}

int isBallCollectedAtCenter() {
  int center = SensorValue[centerLight];

  if (center <= CENTER_BALL_COLLECT_MIN) {
    return 0;
  }

  // If baseline is not ready yet, threshold alone is enough to avoid blocking collection.
  if (!centerBaselineReady) {
    return 1;
  }

  return center >= (centerEmptyBaseline + CENTER_BALL_RISE_MARGIN);
}

void recoverFromBoundary() {
  int currentHeading = -1;

  // Requested behavior: reverse first to clear the corner before rotating.
  setDrive(-DRIVE_BOUNDARY_REVERSE_POWER, -DRIVE_BOUNDARY_REVERSE_POWER);
  wait1Msec(DRIVE_BOUNDARY_REVERSE_MS);

  while (true) {
    if (SensorValue[west] == 0) {
      break;
    }

    currentHeading = readCompassDirectionIndex();
    if (currentHeading < 0) {
      rotateCW(BOUNDARY_ALIGN_TURN_POWER);
      wait1Msec(40);
      continue;
    } else if (currentHeading == 3) {
      break;
    } else {
      // Simple constant rotation until WEST is detected.
      rotateCW(BOUNDARY_ALIGN_TURN_POWER);
    }

    wait1Msec(40);
  }

  stopDrive();
  wait1Msec(120);
}

void avoidObstacle() {
  setDrive(-DRIVE_CREEP_POWER, -DRIVE_CREEP_POWER);
  wait1Msec(220);

  if (scanDirection > 0) {
    rotateCW(DRIVE_AVOID_POWER);
  } else {
    rotateCCW(DRIVE_AVOID_POWER);
  }
  wait1Msec(260);

  stopDrive();
  scanDirection = -scanDirection;
}

void scanForBallStep() {
  if ((nSysTime - lastScanSwapTime) > 900) {
    scanDirection = -scanDirection;
    lastScanSwapTime = nSysTime;
  }

  if (scanDirection > 0) {
    rotateCCW(DRIVE_SCAN_POWER);
  } else {
    rotateCW(DRIVE_SCAN_POWER);
  }
}

int acquireInitialBallTarget(int timeoutMs) {
  int startTime = nSysTime;
  int confirmCount = 0;

  while ((nSysTime - startTime) < timeoutMs) {
    updateCenterBaselineIfEmpty();

    if (isBallCollectedAtCenter()) {
      stopDrive();
      return 1;
    }

    if (anyLineDetected()) {
      recoverFromBoundary();
      continue;
    }

    if (isBallCandidate()) {
      confirmCount++;
      if (confirmCount >= INITIAL_BALL_CONFIRM_COUNT) {
        stopDrive();
        return 1;
      }
    } else {
      confirmCount = 0;
    }

    if (isObstacleCandidate()) {
      avoidObstacle();
      continue;
    }

    // Initial detection mechanism: rotate and search until a valid ball candidate is found.
    scanForBallStep();
    wait1Msec(25);
  }

  stopDrive();
  return 0;
}

int runBallCollectionRoutine(int timeoutMs) {
  int startTime = nSysTime;
  int lastBallSeen = nSysTime - 1000;

  if (acquireInitialBallTarget(INITIAL_BALL_SEARCH_TIMEOUT_MS)) {
    lastBallSeen = nSysTime;
  } else {
    writeDebugStreamLine("Collection: initial ball lock not found, continue scanning.");
  }

  while ((nSysTime - startTime) < timeoutMs) {
    updateCenterBaselineIfEmpty();

    if (isBallCollectedAtCenter()) {
      stopDrive();
      return 1;
    }

    if (anyLineDetected()) {
      recoverFromBoundary();
      continue;
    }

    if (isBallCandidate()) {
      lastBallSeen = nSysTime;
      setDrive(DRIVE_APPROACH_POWER, DRIVE_APPROACH_POWER);
    } else if (isObstacleCandidate()) {
      avoidObstacle();
    } else if ((nSysTime - lastBallSeen) < 250) {
      setDrive(DRIVE_CREEP_POWER, DRIVE_CREEP_POWER);
    } else {
      scanForBallStep();
    }

    wait1Msec(25);
  }

  stopDrive();
  return 0;
}

int alignToEast(int timeoutMs) {
  int startTime = nSysTime;

  while ((nSysTime - startTime) < timeoutMs) {
    int currentHeading = readCompassDirectionIndex();
    if (currentHeading == 1) {
      stopDrive();
      return 1;
    }

    if (currentHeading < 0) {
      rotateCW(ALIGN_TURN_POWER);
      wait1Msec(80);
      continue;
    }

    // Shortest turn on a 4-direction ring (N,E,S,W).
    int cwStepsToEast = (1 - currentHeading + 4) % 4;
    if (cwStepsToEast <= 2) {
      rotateCW(ALIGN_TURN_POWER);
    } else {
      rotateCCW(ALIGN_TURN_POWER);
    }

    wait1Msec(70);
  }

  stopDrive();
  return 0;
}

int reverseToCollection(int timeoutMs) {
  int startTime = nSysTime;

  while ((nSysTime - startTime) < timeoutMs) {
    int blHit = (SensorValue[backLeftLine] == 0);
    int brHit = (SensorValue[backRightLine] == 0);
    int backNear = (SensorValue[backLight] > BACK_DEPOSIT_TRIGGER_MIN);

    if (backNear) {
      stopDrive();
      return 1;
    }

    if (blHit && !brHit) {
      setDrive(-REVERSE_CORRECT_POWER, -REVERSE_POWER);
    } else if (brHit && !blHit) {
      setDrive(-REVERSE_POWER, -REVERSE_CORRECT_POWER);
    } else {
      setDrive(-REVERSE_POWER, -REVERSE_POWER);
    }

    wait1Msec(30);
  }

  stopDrive();
  return 0;
}

int runBallDepositRoutine() {
  if (!alignToEast(4500)) {
    return 0;
  }

  if (!reverseToCollection(7000)) {
    return 0;
  }

  stopDrive();
  openGate();
  wait1Msec(250);
  closeGate();

  setDrive(65, 65);
  wait1Msec(350);
  stopDrive();

  return 1;
}

task main() {
  clearDebugStream();
  writeDebugStreamLine("Competition main started.");

  stopDrive();
  closeGate(); // Start from a closed gate position.
  runCollector(1);
  lastScanSwapTime = nSysTime;

  while (true) {
    runCollector(1);
    updateCenterBaselineIfEmpty();

    if (anyLineDetected()) {
      recoverFromBoundary();
      continue;
    }

    if (isBallCollectedAtCenter()) {
      writeDebugStreamLine("State: deposit.");
      if (!runBallDepositRoutine()) {
        writeDebugStreamLine("Deposit routine timeout/failure.");
      }
      continue;
    }

    if (isBallCandidate()) {
      writeDebugStreamLine("State: collect.");
      if (!runBallCollectionRoutine(3000)) {
        writeDebugStreamLine("Collection routine timeout/failure.");
      }
      continue;
    }

    if (isObstacleCandidate()) {
      avoidObstacle();
      continue;
    }

    scanForBallStep();
    wait1Msec(25);
  }
}



