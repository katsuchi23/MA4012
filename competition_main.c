int isBoundaryLine(int sensorValue) {
  return (sensorValue == boundaryValue);
}

int frontBoundaryDetected() {
  return (isBoundaryLine(SensorValue[frontLeftLine]) ||
          isBoundaryLine(SensorValue[frontRightLine]));
}

int backBoundaryDetected() {
  return (isBoundaryLine(SensorValue[backLeftLine]) ||
          isBoundaryLine(SensorValue[backRightLine]));
}

int detectBoundaryTrigger() {
  int frontTriggered = frontBoundaryDetected();
  int backTriggered = backBoundaryDetected();

  if (frontTriggered && backTriggered) return BOUNDARY_TRIGGER_BOTH;
  if (frontTriggered) return BOUNDARY_TRIGGER_FRONT;
  if (backTriggered) return BOUNDARY_TRIGGER_BACK;
  return BOUNDARY_TRIGGER_NONE;
}

int isEastAlignedExact() {
  return (SensorValue[east] == 0 &&
          SensorValue[north] == 1 &&
          SensorValue[south] == 1 &&
          SensorValue[west] == 1);
}

void alignToEastForBoundaryRecovery() {
  int stableCount = 0;

  while (stableCount < BOUNDARY_ALIGN_STABLE_SAMPLES) {
    if (isEastAlignedExact()) {
      stableCount++;
      stopDrive();
      wait1Msec(20);
      continue;
    }

    stableCount = 0;
    setDrive(BOUNDARY_ALIGN_TURN_POWER, -BOUNDARY_ALIGN_TURN_POWER);
    wait1Msec(25);
  }

  stopDrive();
  wait1Msec(80);
}

void recoverFromBoundary(int triggerSource) {
  boundaryRecoveryActive = 1;

  // Pure straight recovery only, then align to EAST.
  stopDrive();
  wait1Msec(40);

  if (triggerSource == BOUNDARY_TRIGGER_BACK) {
    setDrive(BOUNDARY_RECOVERY_POWER, BOUNDARY_RECOVERY_POWER);
    wait1Msec(BOUNDARY_BACK_RECOVERY_MS);
  } else {
    setDrive(-BOUNDARY_RECOVERY_POWER, -BOUNDARY_RECOVERY_POWER);
    wait1Msec(BOUNDARY_FRONT_RECOVERY_MS);
  }

  stopDrive();
  wait1Msec(80);
  alignToEastForBoundaryRecovery();
  boundaryRecoveryActive = 0;
}

int checkAndHandleBoundaryRecovery() {
  int trigger = BOUNDARY_TRIGGER_NONE;

  if (boundaryRecoveryActive) {
    return 0;
  }

  trigger = detectBoundaryTrigger();
  if (trigger == BOUNDARY_TRIGGER_NONE) {
    return 0;
  }

  writeDebugStreamLine(
    "BOUNDARY trigger=%d FL=%d FR=%d BL=%d BR=%d -> recovery",
    trigger,
    SensorValue[frontLeftLine],
    SensorValue[frontRightLine],
    SensorValue[backLeftLine],
    SensorValue[backRightLine]
  );
  recoverFromBoundary(trigger);
  return 1;
}

void waitWithBoundaryCheck(int durationMs) {
  int remaining = durationMs;

  while (remaining > 0) {
    checkAndHandleBoundaryRecovery();
    {
      int waitStep = (remaining > 10) ? 10 : remaining;
      wait1Msec(waitStep);
      remaining -= waitStep;
    }
  }
}

