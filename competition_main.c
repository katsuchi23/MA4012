/*
  competition_main.c
  Integrated competition logic:
  - boundary safe movement,
  - ball collection using front-below/front-upper/center sensors,
  - ball deposit using compass + back sensor + gate motor.
*/

// -------------------------
// Tunable thresholds
// -------------------------
const int FRONT_BELOW_BALL_MIN = 1400;
const int FRONT_BELOW_BALL_MAX = 3600;
const int FRONT_UPPER_OBSTACLE_MIN = 1200;
const int CENTER_BALL_INSIDE_MIN = 1600;
const int BACK_COLLECTION_NEAR_MIN = 1700;

// -------------------------
// Tunable motor powers
// -------------------------
const int COLLECTOR_POWER = 127;
const int DRIVE_APPROACH_POWER = 95;
const int DRIVE_CREEP_POWER = 65;
const int DRIVE_SCAN_POWER = 55;
const int DRIVE_AVOID_POWER = 75;
const int DRIVE_BOUNDARY_REVERSE_POWER = 90;
const int DRIVE_BOUNDARY_TURN_POWER = 85;
const int ALIGN_TURN_POWER = 65;
const int REVERSE_POWER = 80;
const int REVERSE_CORRECT_POWER = 55;
const int GATE_OPEN_SPEED = 100;
const int GATE_CLOSE_SPEED = -100;
const int GATE_MOVE_MS = 550;

// Compass heading index order: N, NE, E, SE, S, SW, W, NW
const int HEADING_NORTH = 0;

int boundaryTurnRight = 1;
int scanDirection = 1; // 1 = CCW scan, -1 = CW scan
int lastScanSwapTime = 0;

void setDrive(int leftPower, int rightPower) {
  motor[leftWheel] = leftPower;
  motor[rightWheel] = rightPower;
}

void stopDrive() {
  setDrive(0, 0);
}

void rotateCW(int power) {
  setDrive(power, -power);
}

void rotateCCW(int power) {
  setDrive(-power, power);
}

void runCollector(int enabled) {
  if (enabled) {
    motor[collectorMotor] = COLLECTOR_POWER;
  } else {
    motor[collectorMotor] = 0;
  }
}

void openGate() {
  motor[gateMotor] = GATE_OPEN_SPEED;
  wait1Msec(GATE_MOVE_MS);
  motor[gateMotor] = 0;
}

void closeGate() {
  motor[gateMotor] = GATE_CLOSE_SPEED;
  wait1Msec(GATE_MOVE_MS);
  motor[gateMotor] = 0;
}

int anyLineDetected() {
  return (SensorValue[frontLeftLine] ||
          SensorValue[frontRightLine] ||
          SensorValue[backLeftLine] ||
          SensorValue[backRightLine]);
}

int isBallCandidate() {
  int lower = SensorValue[frontBelowLight];
  int upper = SensorValue[frontUpperLight];
  return (lower >= FRONT_BELOW_BALL_MIN &&
          lower <= FRONT_BELOW_BALL_MAX &&
          upper < FRONT_UPPER_OBSTACLE_MIN);
}

int isObstacleCandidate() {
  return SensorValue[frontUpperLight] >= FRONT_UPPER_OBSTACLE_MIN;
}

int isBallCollectedAtCenter() {
  return SensorValue[centerLight] >= CENTER_BALL_INSIDE_MIN;
}

void recoverFromBoundary() {
  int fl = SensorValue[frontLeftLine];
  int fr = SensorValue[frontRightLine];
  int bl = SensorValue[backLeftLine];
  int br = SensorValue[backRightLine];

  setDrive(-DRIVE_BOUNDARY_REVERSE_POWER, -DRIVE_BOUNDARY_REVERSE_POWER);
  wait1Msec(350);

  if ((fl && fr) || (bl && br) || ((fl || bl) && (fr || br))) {
    if (boundaryTurnRight) {
      rotateCW(DRIVE_BOUNDARY_TURN_POWER);
    } else {
      rotateCCW(DRIVE_BOUNDARY_TURN_POWER);
    }
    boundaryTurnRight = !boundaryTurnRight;
    wait1Msec(430);
  } else if (fl || bl) {
    rotateCW(DRIVE_BOUNDARY_TURN_POWER);
    wait1Msec(320);
  } else if (fr || br) {
    rotateCCW(DRIVE_BOUNDARY_TURN_POWER);
    wait1Msec(320);
  }

  stopDrive();
  wait1Msec(120);
}

void avoidObstacle() {
  setDrive(-DRIVE_CREEP_POWER, -DRIVE_CREEP_POWER);
  wait1Msec(220);

  if (scanDirection > 0) {
    rotateCW(DRIVE_AVOID_POWER);
  } else {
    rotateCCW(DRIVE_AVOID_POWER);
  }
  wait1Msec(260);

  stopDrive();
  scanDirection = -scanDirection;
}

void scanForBallStep() {
  if ((nSysTime - lastScanSwapTime) > 900) {
    scanDirection = -scanDirection;
    lastScanSwapTime = nSysTime;
  }

  if (scanDirection > 0) {
    rotateCCW(DRIVE_SCAN_POWER);
  } else {
    rotateCW(DRIVE_SCAN_POWER);
  }
}

int runBallCollectionRoutine(int timeoutMs) {
  int startTime = nSysTime;
  int lastBallSeen = nSysTime;

  while ((nSysTime - startTime) < timeoutMs) {
    if (isBallCollectedAtCenter()) {
      stopDrive();
      return 1;
    }

    if (anyLineDetected()) {
      recoverFromBoundary();
      continue;
    }

    if (isBallCandidate()) {
      lastBallSeen = nSysTime;
      setDrive(DRIVE_APPROACH_POWER, DRIVE_APPROACH_POWER);
    } else if (isObstacleCandidate()) {
      avoidObstacle();
    } else if ((nSysTime - lastBallSeen) < 250) {
      setDrive(DRIVE_CREEP_POWER, DRIVE_CREEP_POWER);
    } else {
      scanForBallStep();
    }

    wait1Msec(25);
  }

  stopDrive();
  return 0;
}

int readCompassCode() {
  return (SensorValue[compassMSB] << 3) +
         (SensorValue[compassBit2] << 2) +
         (SensorValue[compassBit1] << 1) +
         SensorValue[compassLSB];
}

int compassCodeToIndex(int code) {
  switch (code) {
    case 14: return 0; // N
    case 12: return 1; // NE
    case 13: return 2; // E
    case 9:  return 3; // SE
    case 11: return 4; // S
    case 3:  return 5; // SW
    case 7:  return 6; // W
    case 6:  return 7; // NW
    default: return -1;
  }
}

int readCompassIndex() {
  return compassCodeToIndex(readCompassCode());
}

int alignToHeading(int targetHeading, int timeoutMs) {
  int startTime = nSysTime;

  while ((nSysTime - startTime) < timeoutMs) {
    int currentHeading = readCompassIndex();
    if (currentHeading == targetHeading) {
      stopDrive();
      return 1;
    }

    if (currentHeading < 0) {
      rotateCW(ALIGN_TURN_POWER);
      wait1Msec(80);
      continue;
    }

    // Shortest turn on an 8-direction ring.
    int cwSteps = (targetHeading - currentHeading + 8) % 8;
    if (cwSteps <= 4) {
      rotateCW(ALIGN_TURN_POWER);
    } else {
      rotateCCW(ALIGN_TURN_POWER);
    }

    wait1Msec(70);
  }

  stopDrive();
  return 0;
}

int reverseToCollection(int timeoutMs) {
  int startTime = nSysTime;

  while ((nSysTime - startTime) < timeoutMs) {
    int bl = SensorValue[backLeftLine];
    int br = SensorValue[backRightLine];
    int backNear = (SensorValue[backLight] >= BACK_COLLECTION_NEAR_MIN);

    if (bl && br && backNear) {
      stopDrive();
      return 1;
    }

    if (bl && !br) {
      setDrive(-REVERSE_CORRECT_POWER, -REVERSE_POWER);
    } else if (br && !bl) {
      setDrive(-REVERSE_POWER, -REVERSE_CORRECT_POWER);
    } else {
      setDrive(-REVERSE_POWER, -REVERSE_POWER);
    }

    wait1Msec(30);
  }

  stopDrive();
  return 0;
}

int runBallDepositRoutine() {
  if (!alignToHeading(HEADING_NORTH, 4500)) {
    return 0;
  }

  if (!reverseToCollection(7000)) {
    return 0;
  }

  stopDrive();
  openGate();
  wait1Msec(250);
  closeGate();

  setDrive(65, 65);
  wait1Msec(350);
  stopDrive();

  return 1;
}

task main() {
  clearDebugStream();
  writeDebugStreamLine("Competition main started.");

  stopDrive();
  closeGate(); // Start from a closed gate position.
  runCollector(1);
  lastScanSwapTime = nSysTime;

  while (true) {
    runCollector(1);

    if (anyLineDetected()) {
      recoverFromBoundary();
      continue;
    }

    if (isBallCollectedAtCenter()) {
      writeDebugStreamLine("State: deposit.");
      if (!runBallDepositRoutine()) {
        writeDebugStreamLine("Deposit routine timeout/failure.");
      }
      continue;
    }

    if (isBallCandidate()) {
      writeDebugStreamLine("State: collect.");
      if (!runBallCollectionRoutine(3000)) {
        writeDebugStreamLine("Collection routine timeout/failure.");
      }
      continue;
    }

    if (isObstacleCandidate()) {
      avoidObstacle();
      continue;
    }

    scanForBallStep();
    wait1Msec(25);
  }
}
