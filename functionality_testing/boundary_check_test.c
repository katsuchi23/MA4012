/*
  boundary_check_test.c
  Roomba-style boundary behavior:
  move forward -> detect line -> reverse and turn -> continue forward.
*/

const int FORWARD_POWER = 90;
const int REVERSE_POWER = 90;
const int TURN_POWER = 85;

int cornerTurnRight = 1;

void setDrive(int leftPower, int rightPower) {
  motor[leftWheel] = leftPower;
  motor[rightWheel] = rightPower;
}

void stopDrive() {
  setDrive(0, 0);
}

void turnLeftInPlace(int power) {
  setDrive(-power, power);
}

void turnRightInPlace(int power) {
  setDrive(power, -power);
}

int anyLineDetected() {
  return (SensorValue[frontLeftLine] ||
          SensorValue[frontRightLine] ||
          SensorValue[backLeftLine] ||
          SensorValue[backRightLine]);
}

void recoverFromBoundary() {
  int fl = SensorValue[frontLeftLine];
  int fr = SensorValue[frontRightLine];
  int bl = SensorValue[backLeftLine];
  int br = SensorValue[backRightLine];

  stopDrive();
  wait1Msec(120);

  setDrive(-REVERSE_POWER, -REVERSE_POWER);
  wait1Msec(380);

  if ((fl && fr) || (bl && br) || ((fl || bl) && (fr || br))) {
    if (cornerTurnRight) {
      turnRightInPlace(TURN_POWER);
    } else {
      turnLeftInPlace(TURN_POWER);
    }
    cornerTurnRight = !cornerTurnRight;
    wait1Msec(460);
  } else if (fl || bl) {
    turnRightInPlace(TURN_POWER);
    wait1Msec(320);
  } else if (fr || br) {
    turnLeftInPlace(TURN_POWER);
    wait1Msec(320);
  }

  stopDrive();
  wait1Msec(100);
}

task main() {
  clearDebugStream();
  writeDebugStreamLine("Boundary check test started.");

  while (true) {
    if (anyLineDetected()) {
      writeDebugStreamLine(
        "Boundary hit FL=%d FR=%d BL=%d BR=%d",
        SensorValue[frontLeftLine],
        SensorValue[frontRightLine],
        SensorValue[backLeftLine],
        SensorValue[backRightLine]
      );
      recoverFromBoundary();
    } else {
      setDrive(FORWARD_POWER, FORWARD_POWER);
    }

    wait1Msec(25);
  }
}
